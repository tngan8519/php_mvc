function checked_book_manager(){
	checked = document.getElementsByClassName("checked_book_manager");
	for(var i = 0 ; i < checked.length; i++){
		if (checked[i].checked == false)
			checked[i].checked = true;
		else
			checked[i].checked = false;
	}
}
